class Solution {
    void nextPermutation(int[] arr) {
        int n = arr.length;
        
        // Find non-increasing suffix
        int i = n - 2;
        while (i >= 0 && arr[i + 1] <= arr[i]) {
            i--;
        }
        
        if (i >= 0) {
            // Find successor to pivot
            int j = n - 1;
            while (arr[j] <= arr[i]) {
                j--;
            }
            
            // Swap pivot with successor
            int temp = arr[i];
            arr[i] = arr[j];
            arr[j] = temp;
        }
        
        // Reverse suffix
        int left = i + 1;
        int right = n - 1;
        while (left < right) {
            int temp = arr[left];
            arr[left] = arr[right];
            arr[right] = temp;
            left++;
            right--;
        }
    }
}




