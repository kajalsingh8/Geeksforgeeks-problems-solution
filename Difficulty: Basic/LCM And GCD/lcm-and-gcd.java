class Solution {
    static Long[] lcmAndGcd(Long A , Long B) {
        // code here
        Long a = A;
        Long b = B;
        
        // Calculate GCD using Euclidean algorithm
        while (a > 0 && b > 0) {
            if (a > b) {
                a = a % b;
            } else {
                b = b % a;
            }
        }
        
        Long gcd = (a == 0) ? b : a;
        
        // Calculate LCM using the formula LCM(A, B) = (A * B) / GCD(A, B)
        Long lcm = (A * B) / gcd;
        
        // Return the result as an array
        return new Long[]{lcm, gcd};
    }
}
